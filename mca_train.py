        img_files = sorted([f for f in os.listdir(seq_path) if f.lower().endswith(('.jpg', '.png'))])

        # 라벨 및 존재 여부 로드
        with open(os.path.join(seq_path, 'IR_label.json'), 'r') as f:
            label_data = json.load(f)
            gt_boxes = label_data['gt_rect']
            # 만약 exist 키가 없다면 모두 1(존재함)로 간주
            exists = label_data.get('exist', [1] * len(gt_boxes))

        # 1. 초기화 (첫 프레임)
        first_frame = cv2.imread(os.path.join(seq_path, img_files[0]))
        model.init(first_frame, gt_boxes[0])
        current_bbox = gt_boxes[0]

        # 영상 저장을 위한 준비
        h, w, _ = first_frame.shape
        out = cv2.VideoWriter(f"{save_dir}/{seq_name}.mp4", cv2.VideoWriter_fourcc(*'mp4v'), 20, (w, h))

        # 2. 순차 추적 루프 (tqdm으로 진행률 표시)
        for i in tqdm(range(len(img_files)), desc=f"Processing Frames"):
            frame = cv2.imread(os.path.join(seq_path, img_files[i]))
            if frame is None: break

            if i > 0:
                # 💡 핵심: track 실행 후 None 처리 (에러 방어막)
                result = model.track(frame, current_bbox)

                if result is None:
                    # 추적에 실패하거나 모델이 None을 뱉으면, 직전 프레임의 박스를 그대로 유지
                    pred_bbox = current_bbox
                else:
                    # 만약 모델이 (bbox, score) 형태 등 여러 개를 리턴한다면 result[0]로 수정 필요
                    pred_bbox = result

                current_bbox = pred_bbox
            else:
                pred_bbox = gt_boxes[0] # 첫 프레임은 정답 박스 사용

            # 3. 영상에 결과 그리기 (try-except로 좌표 오류 방어)
            try:
                # 🟢 모델 예측값 그리기 (녹색 두꺼운 선)
                if len(pred_bbox) >= 4:
                    px, py, pw, ph = map(int, pred_bbox[:4])
                    cv2.rectangle(frame, (px, py), (px + pw, py + ph), (0, 255, 0), 2)

                # 🔴 실제 정답(GT) 그리기 (빨간색 얇은 선) - 모델 성능 비교용!
                if i < len(gt_boxes) and exists[i] == 1:
                    gx, gy, gw, gh = map(int, gt_boxes[i])
                    cv2.rectangle(frame, (gx, gy), (gx + gw, gy + gh), (0, 0, 255), 1)
            except Exception as e:
                # 박스 좌표가 음수이거나 이상해도 코드가 뻗지 않고 그냥 넘어가게 처리
                pass

            # 프레임 번호 텍스트
            cv2.putText(frame, f"Frame: {i}", (10, 30), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (255, 255, 255), 2)
            out.write(frame)

        out.release()
        print(f"✅ 저장 완료: /content/test_results/{seq_name}.mp4")

