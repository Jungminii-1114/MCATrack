def train_and_val(model, train_loader, val_loader, epochs, device, project_name="MCA_Track_UAV"):
    # 1. WandB 프로젝트 시작 (실험 이름 설정)
    wandb.init(
        project=project_name,
        config={
            "learning_rate": 1e-4,
            "architecture": "MCATracker",
            "dataset": "Anti-UAV410",
            "epochs": epochs,
        }
    )

    optimizer = optim.AdamW(model.parameters(), lr=1e-4, weight_decay=1e-4)

    for epoch in range(epochs):
        model.train()
        train_loss = 0.0

        print(f"\nEpoch {epoch+1}/{epochs} [Train]")
        pbar = tqdm(train_loader)
        for batch in pbar:
            optimizer.zero_grad()

            init_frame = batch['init_frame'][0].numpy()
            init_bbox = batch['init_bbox'][0].numpy()
            search_frame = batch['search_frame'][0].numpy()
            gt_bbox = batch['search_gt_bbox'][0].to(device)

            model.init(init_frame, init_bbox)
            cls_scores, reg_deltas, base_anchors = model.train_track(search_frame, init_bbox)

            loss, l_cls, l_reg = compute_loss(cls_scores, reg_deltas, base_anchors, gt_bbox, device)

            if torch.isinf(loss) or torch.isnan(loss):
                print(f"INF Loss Found -> skipping..{loss.item()}")
                continue

            loss.backward()
            optimizer.step()

            current_loss = loss.item()
            train_loss += current_loss

            wandb.log({
                "batch_loss": current_loss,
                "batch_cls_loss": l_cls.item(),
                "batch_reg_loss": l_reg.item()
            })

            pbar.set_description(f"Loss: {current_loss:.4f}")

        avg_train_loss = train_loss / len(train_loader)

        wandb.log({"epoch": epoch + 1, "avg_train_loss": avg_train_loss})
        print(f"Epoch {epoch+1} Average Loss: {avg_train_loss:.4f}")

        torch.save(model.state_dict(), f'/content/drive/MyDrive/mcatrack_epoch_{epoch+1}.pth')

    wandb.finish()

