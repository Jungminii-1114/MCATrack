def test_tracker_final(model, device, test_dir='/content/data/Anti-UAV410/test'):
    model.eval()
    save_dir = '/content/test_results'
    os.makedirs(save_dir, exist_ok=True)

    # 테스트 폴더 내의 비디오 시퀀스들을 하나씩 순회
